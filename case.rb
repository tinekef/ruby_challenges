weather = 'rainy'

case weather
when 'sunny'
puts "Don´t forget your suncream!"
when 'windy'
puts "Don´t wear a skirt!"
when 'rainy'
puts "Make sure to put some rainboots and bring an umbrella!"
when 'snowy'
puts "Be prepared to build a snow doll, bring your gloves!"
else
puts "Hmm, not sure what to wear in this weather!"
end
